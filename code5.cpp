//User input in cpp

#include <iostream>
using namespace std;

int main() {
    int a;
    int b;

    cout << "Enter first number: ";
    cin >> a; // User input for first number
    cout << "Enter second number: ";
    cin >> b; // User input for second number
    cout << "Sum = " << (a+b) << endl; // Output the sum of the two numbers
    cout << "Difference = " << (a-b) << endl; // Output the difference
}